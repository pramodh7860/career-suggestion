// Add loading animation for images/icons
function preloadIcons() {
    const icons = document.querySelectorAll('.feature-icon, .value-icon, .member-avatar');
    icons.forEach(icon => {
        icon.style.opacity = '0';
        icon.style.transform = 'scale(0.8)';
        
        setTimeout(() => {
            icon.style.transition = 'all 0.5s ease-out';
            icon.style.opacity = '1';
            icon.style.transform = 'scale(1)';
        }, Math.random() * 500);
    });
}

// Initialize icon animations when page loads
window.addEventListener('load', preloadIcons);

// Add scroll progress indicator
function addScrollProgress() {
    const progressBar = document.createElement('div');
    progressBar.style.cssText = `
        position: fixed;
        top: 0;
        left: 0;
        width: 0%;
        height: 3px;
        background: linear-gradient(90deg, var(--primary-color), var(--secondary-color));
        z-index: 10000;
        transition: width 0.1s ease;
    `;
    document.body.appendChild(progressBar);
    
    window.addEventListener('scroll', () => {
        const scrolled = (window.pageYOffset / (document.documentElement.scrollHeight - window.innerHeight)) * 100;
        progressBar.style.width = scrolled + '%';
    });
}

